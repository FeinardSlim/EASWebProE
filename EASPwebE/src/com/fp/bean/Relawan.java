package com.fp.bean;

public class Relawan {
	private int id;
	private String nama;
	private String nik;
	private String ttl;
	private String jenis_kelamin;
	private String no_telp;
	private String email;
	
	public int getId() {return id;}
	public void setId(int id_) {id = id_;}
	
	public String getNama() {return nama;}
	public void setNama(String nama_) {nama = nama_;}
	
	public String getNIK() {return nik;}
	public void setNIK(String nik_) {nik = nik_;}
	
	public String getTTL() {return ttl;}
	public void setTTL(String ttl_) {ttl = ttl_;}
	
	public String getJenisKelamin() {return jenis_kelamin;}
	public void setJenisKelamin(String jenis_kelamin_) {jenis_kelamin = jenis_kelamin_;}
	
	public String getNoTelp() {return no_telp;}
	public void setNoTelp(String no_telp_) {no_telp = no_telp_;}
	
	public String getEmail() {return email;}
	public void setEmail(String email_) {email = email_;}
}
